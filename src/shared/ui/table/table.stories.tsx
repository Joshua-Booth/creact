/**
 * A table with action buttons per row using a dropdown menu.
 */
export const Actions = meta.story({
  render: (args) => (
    <Table {...args}>
      <TableCaption>A list of your recent invoices.</TableCaption>
      <TableHeader>
        <TableRow>
          <TableHead className="w-[100px]">Invoice</TableHead>
          <TableHead>Status</TableHead>
          <TableHead>Method</TableHead>
          <TableHead className="text-right">Amount</TableHead>
          <TableHead className="w-[50px]">
            <span className="sr-only">Actions</span>
          </TableHead>
        </TableRow>
      </TableHeader>
      <TableBody>
        {invoices.map((invoice) => (
          <TableRow key={invoice.invoice}>
            <TableCell className="font-medium">{invoice.invoice}</TableCell>
            <TableCell>{invoice.paymentStatus}</TableCell>
            <TableCell>{invoice.paymentMethod}</TableCell>
            <TableCell className="text-right">{invoice.totalAmount}</TableCell>
            <TableCell>
              <DropdownMenu>
                <DropdownMenuTrigger
                  render={<Button variant="ghost" size="icon" />}
                >
                  <MoreHorizontal className="size-4" />
                  <span className="sr-only">Open menu</span>
                </DropdownMenuTrigger>
                <DropdownMenuContent align="end">
                  <DropdownMenuItem>Edit</DropdownMenuItem>
                  <DropdownMenuItem>Duplicate</DropdownMenuItem>
                  <DropdownMenuSeparator />
                  <DropdownMenuItem variant="destructive">
                    Delete
                  </DropdownMenuItem>
                </DropdownMenuContent>
              </DropdownMenu>
            </TableCell>
          </TableRow>
        ))}
      </TableBody>
    </Table>
  ),
});

Actions.test(
  "when opening row actions, should display menu items",
  async ({ canvas, canvasElement, step }) => {
    const body = within(canvasElement.ownerDocument.body);
    const buttons = await canvas.findAllByRole("button", {
      name: /open menu/i,
    });

    await step("click the first row action button", async () => {
      // eslint-disable-next-line @typescript-eslint/no-non-null-assertion -- findAllByRole guarantees non-empty array
      await userEvent.click(buttons[0]!);
    });

    await step("verify menu items render", async () => {
      await expect(
        await body.findByRole("menuitem", { name: /edit/i })
      ).toBeInTheDocument();
      await expect(
        body.getByRole("menuitem", { name: /duplicate/i })
      ).toBeInTheDocument();
      await expect(
        body.getByRole("menuitem", { name: /delete/i })
      ).toBeInTheDocument();
    });
  }
);
