/* eslint-disable better-tailwindcss/no-unknown-classes -- base-ui uses data-activation-direction, data-ending-style, and xs: breakpoint */
import { NavigationMenu as NavigationMenuPrimitive } from "@base-ui/react/navigation-menu";
import { cva } from "class-variance-authority";
import { ChevronDownIcon } from "lucide-react";

import { cn } from "@/shared/lib/utils";

/** Horizontal navigation bar with animated dropdown content panels. Wraps `@base-ui/react/navigation-menu` with smooth transitions. */
function NavigationMenu({
  align = "start",
  className,
  children,
  ...props
}: NavigationMenuPrimitive.Root.Props &
  Pick<NavigationMenuPrimitive.Positioner.Props, "align">) {
  return (
    <NavigationMenuPrimitive.Root
      data-slot="navigation-menu"
      className={cn(
        `group/navigation-menu relative flex max-w-max flex-1 items-center
        justify-center`,
        className
      )}
      {...props}
    >
      {children}
      <NavigationMenuPositioner align={align} />
    </NavigationMenuPrimitive.Root>
  );
}

/** Horizontal list of navigation menu items. */
function NavigationMenuList({
  className,
  ...props
}: React.ComponentPropsWithRef<typeof NavigationMenuPrimitive.List>) {
  return (
    <NavigationMenuPrimitive.List
      data-slot="navigation-menu-list"
      // Override invalid aria-orientation attribute from base-ui
      aria-orientation={undefined}
      className={cn(
        "group flex flex-1 list-none items-center justify-center gap-0",
        className
      )}
      {...props}
    />
  );
}

/** Individual item within the navigation menu list. */
function NavigationMenuItem({
  className,
  ...props
}: React.ComponentPropsWithRef<typeof NavigationMenuPrimitive.Item>) {
  return (
    <NavigationMenuPrimitive.Item
      data-slot="navigation-menu-item"
      className={cn("relative", className)}
      {...props}
    />
  );
}

/** Base styles for navigation menu trigger buttons. */
const navigationMenuTriggerStyle = cva(
  `group/navigation-menu-trigger inline-flex h-9 w-max items-center
  justify-center rounded-md bg-background px-4 py-2 text-sm font-medium
  transition-all outline-none hover:bg-muted focus:bg-muted focus-visible:ring-3
  focus-visible:ring-ring/50 focus-visible:outline-1
  disabled:pointer-events-none disabled:opacity-50 data-popup-open:bg-muted/50
  data-popup-open:hover:bg-muted data-open:bg-muted/50 data-open:hover:bg-muted
  data-open:focus:bg-muted`
);

/** Button that opens a navigation menu dropdown panel on hover. */
function NavigationMenuTrigger({
  className,
  children,
  ...props
}: NavigationMenuPrimitive.Trigger.Props) {
  return (
    <NavigationMenuPrimitive.Trigger
      data-slot="navigation-menu-trigger"
      className={cn(navigationMenuTriggerStyle(), "group", className)}
      {...props}
    >
      {children}{" "}
      <ChevronDownIcon
        className="relative top-px ml-1 size-3 transition duration-300
          group-data-open/navigation-menu-trigger:rotate-180
          group-data-popup-open/navigation-menu-trigger:rotate-180"
        aria-hidden="true"
      />
    </NavigationMenuPrimitive.Trigger>
  );
}

/** Animated content panel that slides in when its trigger is activated. */
function NavigationMenuContent({
  className,
  ...props
}: NavigationMenuPrimitive.Content.Props) {
  return (
    <NavigationMenuPrimitive.Content
      data-slot="navigation-menu-content"
      className={cn(
        `group-data-[viewport=false]/navigation-menu:bg-popover
        group-data-[viewport=false]/navigation-menu:text-popover-foreground
        group-data-[viewport=false]/navigation-menu:ring-foreground/10
        data-[motion=from-end]:slide-in-from-right-52
        data-[motion=from-start]:slide-in-from-left-52
        data-[motion=to-end]:slide-out-to-right-52
        data-[motion=to-start]:slide-out-to-left-52
        data-[motion^=from-]:animate-in data-[motion^=from-]:fade-in
        data-[motion^=to-]:animate-out data-[motion^=to-]:fade-out
        group-data-[viewport=false]/navigation-menu:data-open:animate-in
        group-data-[viewport=false]/navigation-menu:data-open:fade-in-0
        group-data-[viewport=false]/navigation-menu:data-open:zoom-in-95
        group-data-[viewport=false]/navigation-menu:data-closed:animate-out
        group-data-[viewport=false]/navigation-menu:data-closed:fade-out-0
        group-data-[viewport=false]/navigation-menu:data-closed:zoom-out-95
        data-ending-style:data-activation-direction=left:translate-x-[50%]
        data-ending-style:data-activation-direction=right:translate-x-[-50%]
        data-starting-style:data-activation-direction=left:translate-x-[-50%]
        data-starting-style:data-activation-direction=right:translate-x-[50%]
        h-full w-auto p-2 pr-2.5 transition-[opacity,transform,translate]
        duration-[0.35s] ease-[cubic-bezier(0.22,1,0.36,1)]
        group-data-[viewport=false]/navigation-menu:rounded-md
        group-data-[viewport=false]/navigation-menu:shadow-sm
        group-data-[viewport=false]/navigation-menu:ring-1
        group-data-[viewport=false]/navigation-menu:duration-300
        data-ending-style:opacity-0 data-starting-style:opacity-0
        **:data-[slot=navigation-menu-link]:focus:ring-0
        **:data-[slot=navigation-menu-link]:focus:outline-none`,
        className
      )}
      {...props}
    />
  );
}

/** Positioned container that anchors the navigation menu popup. */
function NavigationMenuPositioner({
  className,
  side = "bottom",
  sideOffset = 8,
  align = "start",
  alignOffset = 0,
  ...props
}: NavigationMenuPrimitive.Positioner.Props) {
  return (
    <NavigationMenuPrimitive.Portal>
      <NavigationMenuPrimitive.Positioner
        side={side}
        sideOffset={sideOffset}
        align={align}
        alignOffset={alignOffset}
        className={cn(
          `isolate z-50 h-(--positioner-height) w-(--positioner-width)
          max-w-(--available-width) transition-[top,left,right,bottom]
          duration-[0.35s] ease-[cubic-bezier(0.22,1,0.36,1)]
          data-instant:transition-none data-[side=bottom]:before:inset-x-0
          data-[side=bottom]:before:-top-2.5`,
          className
        )}
        {...props}
      >
        <NavigationMenuPrimitive.Popup
          className="bg-popover text-popover-foreground ring-foreground/10
            data-[ending-style]:easing-[ease] xs:w-(--popup-width) relative
            h-(--popup-height) w-(--popup-width) origin-(--transform-origin)
            rounded-lg shadow-sm ring-1
            transition-[opacity,transform,width,height,scale,translate]
            duration-[0.35s] ease-[cubic-bezier(0.22,1,0.36,1)] outline-none
            data-ending-style:scale-90 data-ending-style:opacity-0
            data-ending-style:duration-150 data-starting-style:scale-90
            data-starting-style:opacity-0"
        >
          <NavigationMenuPrimitive.Viewport
            className="relative size-full overflow-hidden"
          />
        </NavigationMenuPrimitive.Popup>
      </NavigationMenuPrimitive.Positioner>
    </NavigationMenuPrimitive.Portal>
  );
}

/** Styled link element within navigation menu content. */
function NavigationMenuLink({
  className,
  ...props
}: NavigationMenuPrimitive.Link.Props) {
  return (
    <NavigationMenuPrimitive.Link
      data-slot="navigation-menu-link"
      className={cn(
        `hover:bg-muted focus:bg-muted focus-visible:ring-ring/50
        data-[active=true]:bg-muted/50 data-[active=true]:hover:bg-muted
        data-[active=true]:focus:bg-muted flex items-center gap-1.5 rounded-sm
        p-2 text-sm transition-all outline-none focus-visible:ring-3
        focus-visible:outline-1 [&_svg:not([class*='size-'])]:size-4`,
        className
      )}
      {...props}
    />
  );
}

/* istanbul ignore start -- Optional indicator component, not used in stories */
/** Visual indicator that highlights the active navigation menu item. */
function NavigationMenuIndicator({
  className,
  ...props
}: React.ComponentPropsWithRef<typeof NavigationMenuPrimitive.Icon>) {
  return (
    <NavigationMenuPrimitive.Icon
      data-slot="navigation-menu-indicator"
      className={cn(
        `data-[state=hidden]:animate-out data-[state=hidden]:fade-out
        data-[state=visible]:animate-in data-[state=visible]:fade-in top-full
        z-1 flex h-1.5 items-end justify-center overflow-hidden`,
        className
      )}
      {...props}
    >
      <div
        className="bg-border relative top-[60%] size-2 rotate-45 rounded-tl-sm
          shadow-md"
      />
    </NavigationMenuPrimitive.Icon>
  );
}
/* istanbul ignore end */

export {
  NavigationMenu,
  NavigationMenuContent,
  NavigationMenuIndicator,
  NavigationMenuItem,
  NavigationMenuLink,
  NavigationMenuList,
  NavigationMenuPositioner,
  NavigationMenuTrigger,
  navigationMenuTriggerStyle,
};
